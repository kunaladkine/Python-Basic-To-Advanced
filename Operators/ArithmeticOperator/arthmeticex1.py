#Program for Demonstrating the Fucntionality of Arithmetic Op
#arthmeticex1.py
a=float(input("Enter Value 1 :"))
b=float(input("Enter Value 2 :"))
print("_"*60)
print("\tResults of Arithmetic Operators")
print("_"*60)
print("\t\tSum({},{})={}".format(a,b,a+b))
print("\t\tSub({},{})={}".format(a,b,a-b))
print("\t\tMul({},{})={}".format(a,b,a*b))
print("\t\tDiv({},{})={}".format(a,b,a/b))
print("\t\tFloorDiv({},{})={}".format(a,b,a//b))
print("\t\tModDiv({},{})={}".format(a,b,a%b))
print("\t\tPower({},{})={}".format(a,b,a**b))
print("_"*60)

# Here a+b,a-b, a*b, a/b, a//b  a%b and a**b are called Arithmetic Expressions.