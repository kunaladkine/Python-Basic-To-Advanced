#Special Example

a=100 and 200       #nonzero value
print(f" {100} and {200} Between Val Output is: {a}")

b=100 and -200      #non zero value
print(f" {100} and {-200} Between Val Output is: {b}")

c=100 and 0         #zero value
print(f" {100} and {0} Between Val Output is: {c}")

d=100 and 200 and -300
print(f" {100},{200} and {-300} Between Val Output is: {d}")

e="JAVA" and "PYTHON"          #String Value
print(f" {"JAVA"} and {"PYTHON"} Between Val Output is: {e}")

f="False" and "True"           #String Value
print(f" {"False"} and {"True"} Between Val Output is: {f}")